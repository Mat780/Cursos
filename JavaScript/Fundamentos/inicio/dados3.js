// Em JavaScript não existe diferença entre INT e FLOAT, tudo é NUMBER

let idade = 31;
console.log(typeof 31);
console.log(typeof idade);

let salario = 123.456;
console.log(typeof salario);

// Boolean padrão
let estaChovendo = false;
let estaEnsolarado = true;

console.log(typeof estaChovendo);
console.log(typeof estaEnsolarado);

// String
console.log(typeof "Teste");
console.log(typeof 'Teste 2');